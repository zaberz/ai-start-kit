  chengyi_daohuo_time date,
  gongyi_price numeric(10,5),
  chenshan_mianliao_xiadan_time date,
  chenshan_mianliao_price numeric(10,5),
  chenshan_changjia text,
  chenshan_xiadan_time date,
  chenshan_daohuo_time date,
  chenshan_gongyi_price integer,
