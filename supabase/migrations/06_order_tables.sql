-- Step 6: 创建订单相关表

CREATE TABLE public."order" (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_created uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  date_created timestamptz,
  user_updated uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  date_updated timestamptz,
  user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  pay_type text,
  remark text,
  real_price numeric(10,2),
  total_price numeric(10,2),
  customer_id bigint REFERENCES public.customer(id) ON DELETE SET NULL,
  customer_name text,
  counselor_name text
);

ALTER TABLE public."order" ENABLE ROW LEVEL SECURITY;

CREATE TABLE public.order_payment (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_created uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  date_created timestamptz NOT NULL DEFAULT now(),
  user_updated uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  date_updated timestamptz,
  order_id uuid REFERENCES public."order"(id) ON DELETE SET NULL,
  price numeric(10,2),
  show_counselor_name text
);

ALTER TABLE public.order_payment ENABLE ROW LEVEL SECURITY;

